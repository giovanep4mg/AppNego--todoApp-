    private void jLabelTooBarSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelTooBarSaveMouseClicked
        // tratar possiveis erros 
        try {
            //se (os campos são validos)
            if(isFieldsValid()){
           
            //cria uma variável "task"
            Task task = new Task();
           
            //pega o id dentro da classe "project", e seta o "idProject" 
            task.setIdProject(project.getId());
        
            //pega o texto dentro da "jTextFieldName", e seta na var"name" dentro da "task"
            task.setName(jTextFieldName.getText());
        
            //pega o texto dentro da "jTextAreaDescription", e seta na var"Description" dentro da "task"
            task.setDescription(jTextAreaDescription.getText());
        
            //pega o texto dentro da "jTextAreaNotes", e seta na var"notes" dentro da "task"
            task.setNotes(jTextAreaNotes.getText());
            
            //vai seta na var"completed" false, dentro da "task"
            task.setIsCompleted(false);
        
            //pega o novo formato de data, e coloca na var"dateFormat" 
            SimpleDateFormat dateFormat =new SimpleDateFormat("dd/MM/y");
            
            //cria var"deadline" , vazia.
            Date deadline = null;
            
            //pega o texto dentro da "jFormattedTextFieldDeadline", 
            //analisa se é o mesmo formato do "dateFormat",e salva na var"deadline".
            deadline = dateFormat.parse(jFormattedTextFieldDeadline.getText());
            
            //seta as informações da deadline, dentro da var"deadline" na  "task"
            task.setDeadline(deadline);
            
            //pega a variável "task" , e salva tudo dentro do "controller", 
            controller.save(task);
            
            //exibe mensagem na "janela da tela de tarefa"
            JOptionPane.showMessageDialog(rootPane, "Tarefa salva com sucesso.");
            //fechando a janela
            this.dispose();
            
            // senão (os campos não são validos )
            }else{ 
             //inicia metodo "ocultar campos de erros"
             hideErrorFields();
                    //se (campo de nome, estiver vazio) 
                    if ( jTextFieldName . getText (). isEmpty ()) {
                        //mostra a "janela" jLabelNomeObrigatorio
                        jLabelNomeObrigatorio . setVisible ( true );
                    }
                    //se (campo de data, estiver vazio) 
                    if ( jFormattedTextFieldDeadline . getText (). isEmpty ()) {
                        // mostra a "janela" jLabelPrazoObrigatorio
                        jLabelPrazoObrigatorio . setVisible ( true );
                    }
            }
          // se deu alguma erro , mostra essa mensagem.  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        
         
    }//GEN-LAST:event_jLabelTooBarSaveMouseClicked

