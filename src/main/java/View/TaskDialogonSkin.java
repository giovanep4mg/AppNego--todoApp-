 // metodo verificar se o campos sao válidos       
 public  boolean  isFieldsValid (){
        //o campo de texto da data está vazio e campo de texto do nome está vazio
        return (! jTextFieldName . getText (). isEmpty ()) 
                && (! jFormattedTextFieldDeadline .
                getText (). isEmpty ());
    }
//metodo que fará atualizações das tarefas
public  void  setTasksUpdate ( Task  task ){
        //pega o nome na tarefa, coloca no jTextFieldName
        jTextFieldName. setText( task.getName());
        
        jTextAreaDescription.setText(task.getDescription());
        jTextAreaNotes. setText (task.getNotes());
        SimpleDateFormat  dateFormat = new  SimpleDateFormat ( "dd/MM/aaaa" );
        String  deadline = dateFormat .format(task. getDeadline ());
        jFormattedTextFieldDeadline . setText ("prazo");
        indexTask = task.getId();
        isUpdate= true ;
    }





